# 这是一个Python注释，以#开头的行不会被执行

# print() 函数用于在控制台输出内容
print("Hello, World!")

# 变量定义和使用
# Python中的变量不需要声明类型，会自动推断
name = "Python初学者"  # 字符串类型
age = 2023  # 整数类型

# 使用格式化字符串(f-string)来组合变量和字符串
print(f"你好，{name}！现在是{age}年")

# 基本数据类型演示
my_integer = 42  # 整数
my_float = 3.14  # 浮点数
my_boolean = True  # 布尔值
my_string = "这是一个字符串"  # 字符串

# 打印不同类型的数据
print(f"\n数据类型演示：")
print(f"整数: {my_integer}")
print(f"浮点数: {my_float}")
print(f"布尔值: {my_boolean}")
print(f"字符串: {my_string}")